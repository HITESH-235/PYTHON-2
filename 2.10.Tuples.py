#Tuples are just like lists. Since, lists are mutable, means they can be changed, tuples are immutable, meaning they can't be changed, and they use ().

x = [100]

y = (100,)#always put a comma even if their is just one element.(in tuples)

z = ()#this is how empty lists are written.

print(type(x))

print(type(y))

print(type(z))



